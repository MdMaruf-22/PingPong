package com.example.pingpong;

public class Velocity {
    private int x,y;
    public Velocity(int i, int i1) {
        this.x=i;
        this.y=i1;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
